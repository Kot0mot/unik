def insertion_sort(arr):
    """
    Функция сортировки вставками.
    Сортирует массив на месте.
    """
    # Проходим по всем элементам массива, начиная со второго
    for i in range(1, len(arr)):
        current = arr[i]  # Текущий элемент для вставки
        j = i - 1         # Индекс предыдущего элемента

        # Сдвигаем элементы большие текущего вправо
        while j >= 0 and arr[j] > current:
            arr[j + 1] = arr[j]  # Перемещаем элемент вперед
            j -= 1               # Двигаемся к началу массива

        # Помещаем текущий элемент на правильную позицию
        arr[j + 1] = current

def print_array(arr):
    """Функция для вывода массива."""
    print(*arr)

# Пример использования
if __name__ == "__main__":
    # Создаем тестовый массив
    array = [12, 11, 13, 5, 6]

    print("Исходный массив:")
    print_array(array)

    # Сортируем массив
    insertion_sort(array)

    print("Отсортированный массив:")
    print_array(array)
